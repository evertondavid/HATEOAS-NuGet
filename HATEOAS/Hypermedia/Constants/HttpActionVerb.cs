
namespace HATEOAS.Hypermedia.Constants
{
    /// <summary>
    /// Represents the HTTP action verbs used in RESTful APIs.
    /// </summary>
    public sealed class HttpActionVerb
    {
        /// <summary>
        /// Represents the HTTP GET action verb.
        /// </summary>
        public const string GET = "GET";

        /// <summary>
        /// Represents the HTTP POST action verb.
        /// </summary>
        public const string POST = "POST";

        /// <summary>
        /// Represents the HTTP PUT action verb.
        /// </summary>
        public const string PUT = "PUT";

        /// <summary>
        /// Represents the HTTP DELETE action verb.
        /// </summary>
        public const string DELETE = "DELETE";

        /// <summary>
        /// Represents the HTTP PATCH action verb.
        /// </summary>
        public const string PATCH = "PATCH";

        /// <summary>
        /// Represents the HTTP OPTIONS action verb.
        /// </summary>
        public const string OPTIONS = "OPTIONS";

        /// <summary>
        /// Represents the HTTP HEAD action verb.
        /// </summary>
        public const string HEAD = "HEAD";

        /// <summary>
        /// Represents the HTTP TRACE action verb.
        /// </summary>
        public const string TRACE = "TRACE";

        /// <summary>
        /// Represents the HTTP CONNECT action verb.
        /// </summary>
        public const string CONNECT = "CONNECT";
    }
}
